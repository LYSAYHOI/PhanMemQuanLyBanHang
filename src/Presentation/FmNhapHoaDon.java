    //đổ thông tin về sản phẩm vào table sản phẩm
    public void showtable(){
        DefaultTableModel model=(DefaultTableModel)TbSanPham.getModel();
        int RowCount=model.getRowCount();
        for(int i=0;i<RowCount;i++){
            model.removeRow(RowCount-i-1);
        }
        TableHang modelhang = new TableHang((DefaultTableModel)TbSanPham.getModel());
        TbSanPham.setModel(modelhang.model(""));
    }
    //đổ thông tin về sản phẩm vào table sản phẩm được tìm kiếm theo tên
    public void resetTableSanPham(){
        DefaultTableModel model=(DefaultTableModel)TbSanPham.getModel();
        int RowCount=model.getRowCount();
        for(int i=0;i<RowCount;i++){
            model.removeRow(RowCount-i-1);
        }
        TableHang modelhang = new TableHang(model);
        TbSanPham.setModel(modelhang.model(txtKeySearch.getText()));
    }
    //làm trống giỏ hàng
    public void resetTableGiaHang(){
        DefaultTableModel model=(DefaultTableModel)TbGioHang.getModel();
        int RowCount=model.getRowCount();
        for(int i=0;i<RowCount;i++){
            model.removeRow(RowCount-i-1);
        }
        TbGioHang.setModel(model);
    }
