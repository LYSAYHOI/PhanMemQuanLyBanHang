    private void jLabel3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MousePressed
         if(TxtTenNhanVien.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please choose a employee");
            return;
        }
        if(TbGioHang.getRowCount()==0){
            JOptionPane.showMessageDialog(rootPane, "Shopping cart is empty");
            return;
        }
        int idKH;
        if(TxtTenKH.isEditable()){
            if(TxtCMND.getText().length()==9)
                idKH = new CreateCus(TxtTenKH.getText(), TxtSDT.getText(), TxtCMND.getText(), TxtDiaChi.getText()).Action();
            else {
                JOptionPane.showMessageDialog(rootPane, "Identity Card must be contain 9 digits");
                return;
            }
            if(idKH<1){
                JOptionPane.showMessageDialog(rootPane, "Error to add a customer");
                return;
            }
        }
        int idHD;
        CreateHD createhd= new CreateHD(Float.parseFloat(TxtTong.getText()), TxtTenKH.getText(), TxtTenNhanVien.getText());
        idHD=createhd.Action();
        if(idHD>0)
        {
            String IDHD="HD"+idHD;
            for(int i=0;i<TbGioHang.getRowCount();i++){
                createhd.InsertChiTiet(IDHD,String.valueOf(TbGioHang.getValueAt(i, 0)),
                    Integer.parseInt(String.valueOf(TbGioHang.getValueAt(i, 1))),
                    Float.parseFloat(String.valueOf(TbGioHang.getValueAt(i, 2))));
            }
            
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            
            List<String> InfoKhachHang = new ArrayList<>();
            InfoKhachHang.add(TxtTenKH.getText());
            InfoKhachHang.add(TxtSDT.getText());
            InfoKhachHang.add(TxtCMND.getText());
            InfoKhachHang.add(TxtDiaChi.getText());
            InfoKhachHang.add(TxtTong.getText());
            List<List<String>> InfoHang = new ArrayList<List<String>>();
            for(int i=0;i<TbGioHang.getRowCount();i++){
                List<String> mylist = new ArrayList<>();
                mylist.add(String.valueOf(TbGioHang.getValueAt(i, 0)));
                mylist.add(String.valueOf(TbGioHang.getValueAt(i, 1)));
                BigDecimal soluong = new BigDecimal(String.valueOf(TbGioHang.getValueAt(i, 1)));
                BigDecimal ThanhTien = new BigDecimal(String.valueOf(TbGioHang.getValueAt(i, 2)));
                mylist.add(df.format(ThanhTien.divide(soluong)));
                mylist.add(df.format(TbGioHang.getValueAt(i, 2)));
                InfoHang.add(mylist);
            }
            try{
            new XuatHoaDon(IDHD, InfoKhachHang, InfoHang)
                    .Create();
            }catch(Exception ex){JOptionPane.showMessageDialog(rootPane, "Tạo hóa đơn không thành công");}
            showtable();
            resetTableGiaHang();
        }
        else JOptionPane.showMessageDialog(rootPane, "Không thành công");
    }//GEN-LAST:event_jLabel3MousePressed

