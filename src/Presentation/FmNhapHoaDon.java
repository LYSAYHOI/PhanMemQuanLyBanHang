    //sự kiện click trên table giỏ hàng
    //khi click thì dòng đang click sẽ mất đi hoặc số lượng -1 nếu số lượng >1
    //cập nhập lại tổng giá trong textfield
    private void TbGioHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbGioHangMouseClicked
        int soluong=Integer.parseInt(String.valueOf(TbGioHang.getValueAt(TbGioHang.getSelectedRow(), 1)));
        double Gia1hang=Double.parseDouble(String.valueOf(TbGioHang.getValueAt(TbGioHang.getSelectedRow(), 2)));
        double gia1sp=Gia1hang/soluong;
        TbGioHang.setValueAt(Gia1hang-gia1sp, TbGioHang.getSelectedRow(), 2);
        TxtTong.setText(df.format(Double.parseDouble(TxtTong.getText())-gia1sp));
        if(soluong>1){
            TbGioHang.setValueAt(soluong-1, TbGioHang.getSelectedRow(), 1);
        }else if(soluong<=1){
            DefaultTableModel modelGiaHang = (DefaultTableModel)TbGioHang.getModel();
            modelGiaHang.removeRow(TbGioHang.getSelectedRow());
        }
    }//GEN-LAST:event_TbGioHangMouseClicked
