    private void TbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbSanPhamMouseClicked
        String TenSanPham = String.valueOf(TbSanPham.getValueAt(TbSanPham.getSelectedRow(), 0));
        int SoLuongSanPham = Integer.parseInt(String.valueOf(TbSanPham.getValueAt(TbSanPham.getSelectedRow(), 1)));
        boolean isfound=false;
        double Tong=0;          //biến cộng giá của tất cả các hàng của giỏ hàng
        for (int i = 0; i < TbGioHang.getRowCount(); i++) {         //vòng lặp duyệt tất cả các dòng của giỏ hàng
            int SoLuongGioHang = Integer.parseInt(String.valueOf(TbGioHang.getValueAt(i, 1)));
            if (TenSanPham.equals(String.valueOf(TbGioHang.getValueAt(i, 0)))&& SoLuongSanPham > SoLuongGioHang) {  //tìm thấy và số lượng ở giỏ hàng nhỏ hơn số lượng sản phẩm hiện còn trong kho
                isfound=true;           //biến đánh dấu đã tìm thấy
                TbGioHang.setValueAt(SoLuongGioHang + 1, i, 1);         //trường hợp giỏ hàng đã có hàng thì tăng số lượng sản phẩm
                int SoLuong1SpGiaHang=Integer.parseInt(String.valueOf(TbGioHang.getValueAt(i, 1))); //lấy số lượng 1 sản phẩm trong giỏ
                double GiaSp=Double.parseDouble(String.valueOf(TbSanPham.getValueAt(TbSanPham.getSelectedRow(), 2)));   //lấy giá sp
                TbGioHang.setValueAt(SoLuong1SpGiaHang*GiaSp,i,2);      //set giá trị soluong*gia1sp
            }
            Tong+=Double.parseDouble(String.valueOf(TbGioHang.getValueAt(i, 2)));       //tính tổng giá các dòng của giỏ hàng
            if(TenSanPham.equals(String.valueOf(TbGioHang.getValueAt(i, 0))))       //trường hợp đã bằng số lượng sản phẩm trong kho
            isfound=true;                       //dùng để phân biệt không tìm thấy và tìm thấy nhưng đã bằng số lượng trong kho
        }
        if (isfound == false) {             //trường hợp chưa có sản phẩm trong giỏ hàng thì tạo thêm dòng mới
            DefaultTableModel modelGiaHang = (DefaultTableModel) TbGioHang.getModel();
            double Gia = Double.parseDouble(String.valueOf(TbSanPham.getValueAt(TbSanPham.getSelectedRow(), 2)));
            int SoLuong = 1;        //lần đầu tiên cho số lượng là 1
            modelGiaHang.addRow(new Object[]{TenSanPham, SoLuong, Gia});
            Tong+=Double.parseDouble(String.valueOf(TbSanPham.getValueAt(TbSanPham.getSelectedRow(), 2)));  //tính tổng lượt đầu tiên
        }

        TxtTong.setText(df.format(Tong));
    }//GEN-LAST:event_TbSanPhamMouseClicked
